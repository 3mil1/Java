public class VaryingV {
    public static void main(String[] args) {
        int chickens = 9000;
        double bacon = 0.1;
        String tractor = "Zetor";

        System.out.println(
                "Chickens:" + chickens + " \n" +
                        "Bacon (kg): " + bacon + "\n" +
                        "A tractor: " + tractor
        );
        System.out.println("In a nutshell:");
        System.out.println(chickens);
        System.out.println(bacon);
        System.out.println(tractor);
    }
}
